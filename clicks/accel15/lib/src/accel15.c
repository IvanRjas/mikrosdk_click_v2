void accel15_enable_accel ( accel15_t *ctx ) {
    uint8_t buf[ 1 ];
    
    accel15_generic_read( ctx, ACCEL15_REG_POWER_CTRL, buf, 1 );
    
    buf[ 0 ] |= ACCEL15_X_AXIS_EN;
    
    accel15_generic_write( ctx, ACCEL15_REG_POWER_CTRL, buf, 1 );
}

void accel15_cfg_accel ( accel15_t *ctx, uint8_t acc_odr, uint8_t g_range ) {
    uint8_t buf[ 1 ];
    
    accel15_generic_read( ctx, ACCEL15_REG_ACCEL_CONFIG, buf, 1 );
    
    acc_odr &= ACCEL15_ACCEL_ODR_MSK;
    buf[ 0 ] |= acc_odr;
    
    accel15_generic_write( ctx, ACCEL15_REG_ACCEL_CONFIG, buf, 1 );
    
    accel15_generic_read( ctx, ACCEL15_REG_ACCEL_RANGE, buf, 1 );
    
    g_range &= ACCEL15_ACCEL_RANGE_MSK;
    buf[ 0 ] |= acc_odr;
    
    accel15_generic_write( ctx, ACCEL15_REG_ACCEL_RANGE, buf, 1 );
}

uint8_t accel15_get_internal_status ( accel15_t *ctx ) {
    uint8_t int_status;
    
    accel15_generic_read( ctx, ACCEL15_REG_INTERNAL_STAT, &int_status, 1 );
    
    int_status &= ACCEL15_CONFIG_STREAM_MESSAGE_MSK;
    
    return int_status; 
}

void accel15_get_axis_data ( accel15_t *ctx, accel15_axis_t *axis ) {
    uint8_t rx_buf[ 6 ];
    uint16_t tmp;
    
    accel15_generic_read( ctx, ACCEL15_REG_DATA_8, rx_buf, 6 );
    
    tmp = rx_buf[ 1 ];
    tmp <<= 8;
    tmp |= rx_buf[ 0 ];
    
    axis->x = ( int16_t ) tmp;
    
    tmp = rx_buf[ 3 ];
    tmp <<= 8;
    tmp |= rx_buf[ 2 ];
    
    axis->y = ( int16_t ) tmp;
    
    tmp = rx_buf[ 5 ];
    tmp <<= 8;
    tmp |= rx_buf[ 4 ];
    
    axis->z = ( int16_t ) tmp;
}

